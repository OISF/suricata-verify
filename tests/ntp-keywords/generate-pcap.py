#!/usr/bin/env python3

from scapy.all import Ether, IP, UDP, Raw, wrpcap


# Minimal 48-byte NTP payloads.
# First byte encodes leap/version/mode.
# 0x23 => LI=0, VN=4, Mode=3 (client request)
# 0x24 => LI=0, VN=4, Mode=4 (server reply)
# 0x1b => LI=0, VN=3, Mode=3 (client request)
# 0x1c => LI=0, VN=3, Mode=4 (server reply)
ntp_v4_req = bytes([
    0x23, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x18, 0x57, 0xab, 0xc3, 0x4a, 0x5f, 0x2c, 0xfe,
])
ntp_v4_rsp = bytes([
    0x24, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x4c, 0x4f, 0x43, 0x4c,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x18, 0x57, 0xab, 0xc3, 0x4a, 0x5f, 0x2c, 0xff,
])
ntp_v3_req = bytes([0x1b]) + ntp_v4_req[1:]
ntp_v3_rsp = bytes([0x1c, 0x02]) + ntp_v4_rsp[2:]

pkts = [
    Ether(src="00:11:22:33:44:55", dst="66:77:88:99:aa:bb")
    / IP(src="192.0.2.1", dst="192.0.2.123")
    / UDP(sport=49152, dport=123)
    / Raw(ntp_v4_req),
    Ether(src="66:77:88:99:aa:bb", dst="00:11:22:33:44:55")
    / IP(src="192.0.2.123", dst="192.0.2.1")
    / UDP(sport=123, dport=49152)
    / Raw(ntp_v4_rsp),
    Ether(src="00:11:22:33:44:56", dst="66:77:88:99:aa:bc")
    / IP(src="192.0.2.2", dst="192.0.2.124")
    / UDP(sport=49153, dport=123)
    / Raw(ntp_v3_req),
    Ether(src="66:77:88:99:aa:bc", dst="00:11:22:33:44:56")
    / IP(src="192.0.2.124", dst="192.0.2.2")
    / UDP(sport=123, dport=49153)
    / Raw(ntp_v3_rsp),
]

wrpcap("input.pcap", pkts)
print("wrote input.pcap")
